      <h3>Déroulement d'une séance</h3>
      <p>
        Chaque séance débute par une discussion, un dialogue permettant de déterminer les besoins, les raisons de la prise en charge et le compte-rendu de la séance précédente.
        Une séance se déroule habillé(e) dans une atmosphère de détente. Sa pratique s'intègre à notre vie quotidienne et ne demande ni tenue ni condition particulière.<br/>
        Elle se décompose en deux temps : <br/>
        La première partie dite "relaxation dynamique" (en position debout), se compose de petits exercices synchronisant la respiration et les mouvements. Cette méthode permet un travail sur le schéma corporel et l'image de soi en induisant déjà la détente afin d'initier la suite de la séance.<br/>
        Lors de la deuxième partie de la séance, (allongé(e) ou assis(e) selon les préférences), le sophrologue vous conduit vers un niveau de relaxation se situant entre veille et sommeil grâce à des exercices de respiration et de détente neuro-musculaire.
        A ce stade, il sera possible, grâce à  la visualisation positive entre autre, de stimuler l'imagination, la concentration, et surtout de se (re)centrer sur la problématique du moment. 
        Le retour à l'état de veille se fait en redynamisant les muscles lentement.<br/>
        La séance se termine par l'expression du vécu, un moment de partage et de discussion en toute confiance et confidence.<br/>
        Il s'agit donc de transmettre une méthode, d'enseigner des exercices précis, réutilisables d'une manière autonome chez soi, au travail ou à l'école...<br/>
